for (let y = 0; y < table.length; y++) {
  for (let x = 0; x < table[0].length; x++) {
    let currentChar = table[y][x];
    if (currentChar !== "O") {
      continue;
    }
    //at the top
    if (y === 0) {
      continue;
    }

    //slide up
    var nextY = y - 1;
    while (nextY >= 0 && table[nextY][x] === ".") {
      table[nextY][x] = "O";
      table[nextY + 1][x] = ".";
      nextY -= 1;
    }
  }
}

var total = 0;
for (let y = 0; y < table.length; y++) {
  for (let x = 0; x < table[0].length; x++) {
    if (table[y][x] === "O") {
        total += (table.length - y);
    }
  }
}

