const Direction = {
  N: 0,
  W: 1,
  S: 2,
  E: 3,
};

var table = input.split("\n").map((x) => [...x]);
console.log(table.map((r) => r.join("")).join("\n"));

function tilt(direction) {
  var startY = 0;
  var endY = table.length;
  var yStep = 1;
  var startX = 0;
  var endX = table[0].length;
  var xStep = 1;

  switch (direction) {
    case Direction.N:
      break;
    case Direction.S:
      startY = table.length - 1;
      endY = -1;
      yStep = -1;
      break;
    case Direction.E:
      startX = table[0].length - 1;
      endX = -1;
      xStep = -1;
      break;
    case Direction.W:
      break;
  }

  for (let y = startY; y !== endY; y += yStep) {
    for (let x = startX; x !== endX; x += xStep) {
      let currentChar = table[y][x];
      if (currentChar !== "O") {
        continue;
      }

      var nextX = x;
      var nextY = y;
      var oldX = x;
      var oldY = y;

      oldX = nextX;
      oldY = nextY;
      switch (direction) {
        case Direction.N:
          nextY = y - 1;
          break;
        case Direction.S:
          nextY = y + 1;
          break;
        case Direction.E:
          nextX = x + 1;
          break;
        case Direction.W:
          nextX = x - 1;
          break;
      }

      while (
        nextY >= 0 &&
        nextY < table.length &&
        nextX >= 0 &&
        nextX < table[0].length &&
        table[nextY][nextX] === "."
      ) {
        table[nextY][nextX] = "O";
        table[oldY][oldX] = ".";
        oldX = nextX;
        oldY = nextY;
        switch (direction) {
          case Direction.N:
            nextY = oldY - 1;
            break;
          case Direction.S:
            nextY = oldY + 1;
            break;
          case Direction.E:
            nextX = oldX + 1;
            break;
          case Direction.W:
            nextX = oldX - 1;
            break;
        }
      }
    }
  }
}

